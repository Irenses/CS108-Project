package user;

import java.sql.Timestamp;

public class UserDerp {
	
	private String name;
	private String user;
	
	public UserDerp(String user, String name) {
		this.name = name;
		this.user = user;
	}
	
	public String getUserName() {
		return this.user;
	}
	
	public String getFullName() {
		return this.name;
	}


}
