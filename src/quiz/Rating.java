package quiz;

public class Rating {
	private int quizID;
	private String username;
	private int rating;
	
	public Rating(int quizID, String username, int rating) {
		this.quizID = quizID;
		this.username = username;
		this.rating = rating;
	}
	
	public int getQuizID() {
		return quizID;
	}
	
	public String getUsername() {
		return username;
	}
	
	public int getRating() {
		return rating;
	}
}
